export class Person {
    constructor(id, name, popularity, imagePath, birth, death, placeOfBirth, biography, credits) {
        this.id = id;
        this.name = name;
        this.popularity = popularity;
        this.imagePath = imagePath;
        this.birth = birth;
        this.death = death;
        this.placeOfBirth = placeOfBirth;
        this.biography = biography;
        this.credits = credits;
    }
}